//  The minimum distance funtion for finding minimum distance
int mindistance(int distance[],bool stat[])
{
	int minimum=INT_MAX,ind;
	for(int k=0;k<56;k++)
	{
		if(stat[k]==false && distance[k]<=minimum)
		{
			minimum=distance[k];
			ind=k; // returns index of the vertex with the minimum distance that is not yet included in the tree
		}
	}
	return ind;
}
void dijkstra(int graph[56][56],int source, string stations[55],int dest)
{
	int distance[56]; //initialised an array of size 56 for distance
	bool stat[56]; // for visited
	for(int k=0;k<56;k++)
	{
		distance[k]=INT_MAX; // initiaising whole array with infinity
		stat[k]=false;  // initialising visited with false
	}
	distance[source]=0;
	for(int k=0;k<56;k++)
	{
		int m=mindistance(distance,stat);
		stat[m]=true;
		for(int k=0;k<56;k++)
		{
			if(!stat[k] && graph[m][k] && distance[m]!=INT_MAX && distance[m]+graph[m][k]<distance[k])
				distance[k]=distance[m]+graph[m][k];
		}
	}	
	cout<<"Minimum Number of Stations from "<<stations[source]<<" To every station"<<endl;
	for(int k=0;k<56;k++)
	{
		cout<<"Minimum Stations from "<<stations[source]<<" To "<<stations[k]<<"  are  "<<distance[k]<<endl;
	}
	cout<<endl<<"Minimum Stations from "<<stations[source]<<" To  "<<stations[dest]<<" are "<<distance[dest]<<endl;
}
